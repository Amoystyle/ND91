ND_UCHAR * ND_GetHanziSpell( ND_UINT16 asc )
{
	ND_UINT8 h,l;//asc¸ß×Ö½Ú¡¢µÍ×Ö½Ú
	ND_UINT16 id;//¸ù¾Ýasc¼ÆËã³öºº×ÖÔÚhzToPyE2µÄÎ»ÖÃ£¨ÏÂ±ê£©

	h = asc >> 8;
	l = (ND_UINT8)asc;

	if(h < hzToPyE2.h0 || h > hzToPyE2.h1 || l < hzToPyE2.l0 || l > hzToPyE2.l1)
		return NULL;

	id = (h - hzToPyE2.h0)*(hzToPyE2.l1 - hzToPyE2.l0 + 1) + (l - hzToPyE2.l0);

	if(hzToPyE2.spell[id][0] == '0')
		return NULL;
	else
		return (ND_UCHAR*)hzToPyE2.spell[id];
}